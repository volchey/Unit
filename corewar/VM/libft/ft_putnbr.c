#include "libft.h"

void	ft_putnbr(int n)
{
	long int	ln;
	long int	i;

	ln = n;
	i = 1;
	if (ln < 0)
	{
		ft_putchar('-');
		ln *= -1;
	}
	while (ln / i > 9)
		i *= 10;
	while (i > 0)
	{
		ft_putchar(ln / i + '0');
		ln %= i;
		i /= 10;
	}
}
