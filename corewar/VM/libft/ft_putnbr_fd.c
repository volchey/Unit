#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	ln;
	long int	i;

	ln = n;
	i = 1;
	if (ln < 0)
	{
		ft_putchar_fd('-', fd);
		ln *= -1;
	}
	while (ln / i > 9)
		i *= 10;
	while (i > 0)
	{
		ft_putchar_fd(ln / i + '0', fd);
		ln %= i;
		i /= 10;
	}
}
