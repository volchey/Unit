unsigned int	swap_4bits(unsigned char *buf)
{
	return (buf[3] + (buf[2] << 8) + (buf[1] << 16) + (buf[0] << 24));
}

short			swap_2bits(unsigned char *buf)
{
	return (buf[1] | (buf[0] << 8));
}
