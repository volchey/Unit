#include <stdlib.h>
#include "lem_in.h"

void	arr_del(char **arr)
{
	int i;

	i = 0;
	while ((arr[i]))
	{
		ft_strdel(&arr[i]);
		i++;
	}
	ft_strdel(arr);
	free(arr);
}
